package ports

// ports 包表示 “端口” 的集合，来源于六边形加购的 Ports & Adapters 思想。
// 	端口是系统对外或对下游依赖的 “抽象能力借口”。
//	实现端口的模块成为适配器 ( Adapter )。
//
// 其作用是把系统的 稳定边界“ 抽象成一组最小、清晰的借口 ( 契约 )，
// 让上层用例/领域代码只依赖这些接口，而不依赖任何具体实现或其他的可变服务，
// 避免跨 service 的直接依赖，实现彻底解耦。
//
// 注意：
// 	1.端口要 “窄而稳”，不要泄漏具体 sdk/第三方类型。
//	2.使用 domain 基本类型或自定义 dto。
//	3.端口只放借口和必要的 dto，不放任何实现与初始化逻辑。
//	4.在 ioc 组装层绑定，注入到用例/服务中。
